import time


# ------------------------------------------------------------------------------------------------

class Timer:
    """
    Simple utility to measure elapsed time.
    """

    def __init__(self):
        self._t_start = time.time()


# ------------------------------------------------------------------------------------------------

    def start(self):
        self._t_start = time.time()


# ------------------------------------------------------------------------------------------------

    def elapsed(self):
        return time.time() - self._t_start


# ------------------------------------------------------------------------------------------------

    def print_elapsed(self, str='elapsed {} s.'):
        print(str.format(self.elapsed()))


# ------------------------------------------------------------------------------------------------
